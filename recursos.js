// Helper: resol coord a partir de municipi (preferit) o comarca (fallback).
// Tolera variants amb/sense article inicial (L', La, Les, El, Els).
function resolveCoord(municipi, comarca) {
  if (municipi) {
    if (MUNICIPI_COORDS[municipi]) return MUNICIPI_COORDS[municipi];
    // Prova sense article inicial
    const noArt = municipi.replace(/^(L'|l'|La |la |Les |les |El |el |Els |els )/, '').trim();
    if (noArt !== municipi && MUNICIPI_COORDS[noArt]) return MUNICIPI_COORDS[noArt];
    // Prova afegint article comú
    for (const prefix of ['El ', 'La ', 'Les ', 'L\'']) {
      if (MUNICIPI_COORDS[prefix + municipi]) return MUNICIPI_COORDS[prefix + municipi];
    }
    // Text entre parèntesis (ex: "Bellaterra (Cerdanyola del Vallès)") → prova la part entre parèntesis
    const m = municipi.match(/\(([^)]+)\)/);
    if (m && MUNICIPI_COORDS[m[1]]) return MUNICIPI_COORDS[m[1]];
  }
  if (comarca && COMARCA_COORDS[comarca]) return COMARCA_COORDS[comarca];
  return null;
}

// Helper: genera un slug ASCII d'un text (per a ids).
function slugify(s) {
  return String(s)
    .toLowerCase()
    .normalize('NFD').replace(/[\u0300-\u036f]/g, '')
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '')
    .slice(0, 60);
}

